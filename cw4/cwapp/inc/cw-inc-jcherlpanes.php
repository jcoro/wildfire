<h3>Mucuna (<em>Mucuna pruriens Extract</em> )</h3> 
<p>Mucuna pruriens is a tropical legume which has been used for hundreds of years in Ayurvedic medicine as a nervous&#45;system tonic and aphrodisiac*.  It is often used to elevate mood and to enhance sexual energy*.  Mucuna is one of the few plant sources of l&ndash;dopa, a direct precursor to the "feel&ndash;good" brain chemical, dopamine.</p>

<h3>Damiana Leaves (<em>Turnera diffusa</em> )</h3>    
<p>Damiana leaves have been used traditionally to enhance energy levels, mood, and both male and female sexual health*.  Modern research has shown that Damiana may reduce anxiety and act as an adaptogen* &#8212; a substance which improves the body&#39;s ability to cope with stress.</p>
    
<h3>Catuaba Bark (<em>Trichilia catigua</em> ) </h3>
<p>Catuaba is a Brazilian herb which has been used for centuries as an aphrodisiac, and to combat exhaustion and fatigue*.  Though energizing, Catuaba is also reported to calm the nerves and reduce anxiety*.  Modern research has shown that Catuaba may impart a mood&#45;elevating effect through its actions on the "feel&ndash;good" brain chemical, dopamine*.</p>

<h3>Yohimbe Bark Powder (<em>Pausinystalia yohimbe</em> ) </h3> 
<p>The bark of the African Yohimbe tree has traditionally been used as an aphrodisiac and energy tonic*.  These traditional uses are in keeping with modern research showing that substances in yohimbe may increase levels of energizing brain chemicals such as norepinephrine and dopamine*.  Integrated Supplements Herbal Energy contains a small (but effective) dose of whole yohimbe bark powder &ndash; not a concentrated extract of yohimbe alkaloids like some yohimbe supplements.  Though some concentrated yohimbe extracts can be overly stimulatory in high doses, the small dose of whole yohimbe bark in Herbal Energy is both safe and effective, and is likely to impart a pleasant energizing effect at the recommended dose.</p>   

